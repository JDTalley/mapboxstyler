class Points {
    constructor() {
        this.reset()
    }

    reset() {
        this.radius     = 5
        this.color      = '#000000'
        this.blur       = 0
        this.opacity    = 1

        // Stroke Properties
        this.sColor     = '#000000'
        this.sOpacity   = 1
        this.sWidth     = 0
    }
}